// Print numbers 1 to 10

for(let i=1; i<=10;i++){
    console.log(i)
}

// Print even numbers from 1 to 20

for(let i = 1;i<=20;i++){
    if(i%2==0){
        console.log(i)
    }
}

// Print odd numbers from 1 to 20

for(let i=1;i<=20;i++){
    if(i%2!==0){
        console.log(i)
    }
}

// Print multiplication table of 7

for(let i =1;i<=10;i++){
    console.log(`7 * ${i} =`,7*i)
}

